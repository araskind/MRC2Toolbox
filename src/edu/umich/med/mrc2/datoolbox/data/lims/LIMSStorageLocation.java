package edu.umich.med.mrc2.datoolbox.data.lims;

import java.io.Serializable;

public class LIMSStorageLocation implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8716631224256935637L;
	private String id;
	private String description;
	private String room;
	private String unit;
	
	public LIMSStorageLocation(String id, String description, String room, String unit) {
		super();
		this.id = id;
		this.description = description;
		this.room = room;
		this.unit = unit;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getRoom() {
		return room;
	}

	public void setRoom(String room) {
		this.room = room;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}
}